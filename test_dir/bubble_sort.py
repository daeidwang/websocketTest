def bubble_sort(arr):
    """
    冒泡排序算法

    核心思想：
        重复遍历数组，依次比较相邻的两个元素，如果顺序错误（前者大于后者）则交换位置。
        每一轮遍历会将当前未排序部分的最大值"冒泡"到末尾的正确位置。
        经过 n-1 轮后，整个数组有序。

    时间复杂度：
        - 最坏/平均: O(n²) — 完全逆序时需要 n(n-1)/2 次比较和交换
        - 最好: O(n)     — 数组已有序，借助 swapped 标志一轮即退出

    空间复杂度: O(1) — 原地排序，仅使用常量额外空间

    稳定性: 稳定 — 相等元素不会交换，相对顺序保持不变

    参数:
        arr (list): 待排序的列表

    返回:
        list: 排序后的列表（原地修改，同时返回引用）
    """
    # 获取数组长度
    n = len(arr)

    # 外层循环：控制遍历轮数，最多 n 轮
    for i in range(n):
        # swapped 标志：用于检测本轮是否发生交换
        # 若本轮无交换，说明数组已完全有序，可提前退出
        swapped = False

        # 内层循环：比较相邻元素并交换
        # 每轮结束后，末尾 i 个元素已排好序，无需再比较
        # 因此内层循环范围缩小为 n - 1 - i
        for j in range(n - 1 - i):
            if arr[j] > arr[j + 1]:
                # 交换相邻两个元素（Python 一行交换语法）
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True

        # 如果本轮没有发生任何交换，数组已有序，提前结束
        if not swapped:
            break

    return arr

